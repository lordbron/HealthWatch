#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.tableData.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"heartRateCell" forIndexPath:indexPath];
    // Configure the cell...
    HKQuantitySample *currentSample = [self.tableData objectAtIndex:indexPath.row];
    //cell.textLabel.text = [NSString stringWithFormat:@"%@ %@", currentSample.quantity, currentSample.quantityType];
    double bps = [currentSample.quantity doubleValueForUnit:[[HKUnit countUnit] unitDividedByUnit:[HKUnit minuteUnit]]];
    NSNumberFormatter *formatter = [[NSNumberFormatter alloc] init];
    formatter.roundingIncrement = [NSNumber numberWithDouble:0];
    formatter.numberStyle = NSNumberFormatterDecimalStyle;
    
    cell.textLabel.text = [NSString stringWithFormat:@"%@ bpm - %@", [formatter stringFromNumber:[NSNumber numberWithDouble:bps]], currentSample.sourceRevision.source.name];
    return cell;
}

